/**
  * @brief This function handles Hard fault interrupt.
  *
  * ⚠️ 【增强版】HardFault异常诊断处理
  * 当系统发生致命错误时,输出详细的诊断信息到UART1!
  */
void HardFault_Handler(void)
{
  /* USER CODE BEGIN HardFault_IRQn 0 */

  /*
   * ⚠️ 【致命】系统发生HardFault异常!
   * 直接通过HAL_UART_Transmit发送诊断信息 (不依赖FreeRTOS/日志系统)
   */
  const char *msg1 = "\n\n";
  const char *msg2 = "╔══════════════════════════════════════╗\n";
  const char *msg3 = "║     FATAL: HardFault Exception!      ║\n";
  const char *msg4 = "╚══════════════════════════════════════╝\n\n";

  HAL_UART_Transmit(&huart1, (uint8_t *)msg1, strlen(msg1), 100);
  HAL_UART_Transmit(&huart1, (uint8_t *)msg2, strlen(msg2), 100);
  HAL_UART_Transmit(&huart1, (uint8_t *)msg3, strlen(msg3), 100);
  HAL_UART_Transmit(&huart1, (uint8_t *)msg4, strlen(msg4), 100);

  const char *msg5 = "Possible causes:\n";
  const char *msg6 = "  [1] NULL pointer dereference\n";
  const char *msg7 = "  [2] Stack overflow (check task stack size)\n";
  const char *msg8 = "  [3] Memory corruption (heap/stack collision)\n";
  const char *msg9 = "  [4] FreeRTOS internal error (invalid TCB)\n";
  const char *msg10 = "  [5] osKernelStart() configuration failure\n\n";

  HAL_UART_Transmit(&huart1, (uint8_t *)msg5, strlen(msg5), 100);
  HAL_UART_Transmit(&huart1, (uint8_t *)msg6, strlen(msg6), 100);
  HAL_UART_Transmit(&huart1, (uint8_t *)msg7, strlen(msg7), 100);
  HAL_UART_Transmit(&huart1, (uint8_t *)msg8, strlen(msg8), 100);
  HAL_UART_Transmit(&huart1, (uint8_t *)msg9, strlen(msg9), 100);
  HAL_UART_Transmit(&huart1, (uint8_t *)msg10, strlen(msg10), 100);

  {
          const char msg12[64];
          int len;

          len = snprintf((char *)msg12, sizeof(msg12),
                       "  Time: %lu ms\n", (unsigned long)HAL_GetTick());
          HAL_UART_Transmit(&huart1, (uint8_t *)msg12, len, 100);

          len = snprintf((char *)msg12, sizeof(msg12),
                       "  MSP: 0x%08X\n", (unsigned int)__get_MSP());
          HAL_UART_Transmit(&huart1, (uint8_t *)msg12, len, 100);
  }

  const char *msg13 = "\n>>> System halted. Reset required! <<<\n\n";
  HAL_UART_Transmit(&huart1, (uint8_t *)msg13, strlen(msg13), 100);

  __disable_irq();

  /* USER CODE END HardFault_IRQn 0 */
  while (1)
  {
    /* USER CODE BEGIN W1_HardFault_IRQn 0 */
    /* USER CODE END W1_HardFault_IRQn 0 */
  }
}

